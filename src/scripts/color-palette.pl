sub RGB2Hex {
    $red=$_[0];
    $green=$_[1];
    $blue=$_[2];
    $string=sprintf ("#%2.2X%2.2X%2.2X",$red,$green,$blue);
    return ($string);
}

sub GetParaverColor 
{
  my ($Color) = @_;
  $Color ++;
  if ($Color < 0) 
  {
    $Color = 0;
  }
  if ($Color > $MAX_COLORS) 
  {
    $Color = $MAX_COLORS-1;
  }
  return RGB2Hex($ColorPalette[$Color][0], $ColorPalette[$Color][1], $ColorPalette[$Color][2]);
}

